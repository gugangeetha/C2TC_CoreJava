package com.tns.daytwo;

public class loopingstatementsdaytwo {

	public static void main(String[] args) {
		///for loop
		for (int i=0; i<=10; i++) {
			System.out.println(i);
		}
	
	//while loop
		int h=0;
		while (h < 10) {
			System.out.println(h);
			h++;
		}
		
	//Do While Loop
		int t=0;
		do {
			System.out.println(t);
			t++;
		}
		while(t<3);
		
	}	
}
